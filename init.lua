local function get_greeting()
  local hour = os.date('*t').hour -- Get the current hour in 24-hour format

  if hour >= 2 and hour < 12 then
    return 'Good morning,  '
  elseif hour >= 12 and hour < 17 then
    return 'Good afternoon,'
  else
    return 'Good evening,  '
  end
end
